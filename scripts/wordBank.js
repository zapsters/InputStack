function scramble_word_list_filter(count) {
  var local_masterList = scrambled_words_list_MASTER.map((x) => x);

  //Only use words higher than / equal to 5 or lower than / equal to 8. -- I decided that the character limit was arbitrary
  //scrambled_words_list = local_masterList.filter(word => word.length >= 5);
  //scrambled_words_list = local_masterList.filter(word => word.length <= 8);

  //Shuffle array, then get the first *count* (ex 60) words in array.
  const shuffled = local_masterList.sort(() => 0.5 - Math.random());
  scrambled_words_list = shuffled.slice(0, count);

  scrambled_words_list.sort();
  return scrambled_words_list;
}

